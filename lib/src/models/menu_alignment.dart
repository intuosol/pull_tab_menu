/// Defines the alignment of the pull tab menu.
enum MenuAlignment {
  /// Menu is aligned to the top of the screen.
  topLeft,

  /// Menu is aligned to the center left of the screen.
  centerLeft,

  /// Menu is aligned to the bottom left of the screen.
  bottomLeft,

  /// Menu is aligned to the top right of the screen.
  topRight,

  /// Menu is aligned to the center right of the screen.
  centerRight,

  /// Menu is aligned to the bottom right of the screen.
  bottomRight,
}
